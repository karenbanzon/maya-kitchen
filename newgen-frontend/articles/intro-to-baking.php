                    <p class="articleContent"><strong>Basic Ingredients</strong> 

Flour is the basic ingredient in cakes, pastries, breads and scores of other baked products. It provides the structure or framework of the food. Different types of flour are used for baking although the most commonly used is the all-purpose flour since it can be used for all kinds of baked goods. For cakes, it is best to use cake flour because of its lightness and low protein content while bread flour is the most suitable for breads due to its high protein content. Other flours used in baking include whole wheat flour, pastry flour, etc.

Sugar functions not only as sweetener. It is also responsible for making the cake tender because it hinders in the hydration of flour which is necessary in the development of gluten. Sugar also provides the golden brown color of cakes or breads. Most used is the refined white sugar or granulated sugar although some recipes call for brown sugar and even confectioners&rsquo; sugar.

Fat is also needed for baking because it makes the baked products tender, moist and rich. Butter or margarine is usually preferred because of their flavor and for additional color. Shortening is also often used while others specify oil. Butter can either be creamed or melted depending on its use.

To make cakes rise, leavening agents are added. These produce carbon dioxide largely responsible for the rising of the cake or its volume. They also make the cake light and porous. Baking powder, baking soda and yeast are examples of leaveners used in baking. The first 2 are used for cakes and pastries while yeast is used for breads.

To hold the batter of dough together and to blend all the ingredients, liquid is added. Liquid can be in the form of water, milk or juices. Milk refers to whole cow&rsquo;s milk. To substitute with canned evaporated milk; dilute it in a 1:1 proportion. Powdered full-cream milk can also be used as substitute, simply dissolve it in water before using.

For additional structure, richness and nutrition eggs are added- either whole, just egg yolks or just egg whites. The important thing is to use eggs of the same size.

Lastly, to make cakes flavorful and more interesting, add nuts, dried fruits, flavoring, spices and even fresh fruits.

High Altitude Baking: At 3,000 feet above sea level there is less air pressure and water boils at a lower temperature. Because of this rising of cakes is affected. The volume of gas produced by leaveners is more so there is a need to lessen the baking powder by 1/8 teaspoon for every teaspoon used. Sugar is also reduced by 1 tablespoon per cup used and the liquid increased by 1-2 tablespoons per cup used. As a rule, the baking temperature is increased by 25&deg;F.

