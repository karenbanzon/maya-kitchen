<p class="articleContent">Preheat oven to 350 degrees F. Brush bottom of an 8 x 3â€“inch loaf pan with oil or shortening then line it with wax paper. Cream butter in a bowl until soft. Blend in the eggs and water. Add the cake mix and beat until smooth. Pour mixture into the prepared pan and spread evenly. Bake for 35 to 40 minutes or until done. Set aside.

<strong>Prepare Filling:</strong>
Beat together the cream cheese, syrup, brandy, sugar and egg yolk. Whip the cream separately then fold into cream cheese mixture. Set aside.

<strong>Prepare Syrup:</strong>
Combine the ingredients for the syrup and stir until dissolved.

<strong>To Assemble:</strong>
Place a slice of cake in bottom of parfait glass or wine goblets, tearing and pressing butter cake to fit. Drizzle syrup. Spread 1/3 cup filling over each. Repeat layering with remaining butter cake, syrup and filling. Sift unsweetened cocoa generously over parfait. Cover and refrigerate overnight.</p>
