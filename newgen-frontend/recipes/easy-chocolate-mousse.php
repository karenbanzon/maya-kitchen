                    <h6>Ingredients</h6>
                    Cake:
                    <ul>
                        <li>1 pack MAYA Chocolate Hotcake Mix 200g</li>
                        <li>1 piece egg</li>
                        <li>2 tablespoons oil</li>
                        <li>1/2 cup water</li>
                    </ul>
                    Mousse:
                    <ul>
                        <li>2 tablespoons cold water</li>
                        <li>1 tablespoon unflavored gelatin</li>
                        <li>2 tablespoons cold water</li>
                        <li>4 tablespoons hot water</li>
                        <li>100 grams bitter sweet or semi sweet chocolate chips</li>
                        <li>250 ml all&ndash;purpose cream</li>
                    </ul>
                    Garnish:
                    <ul>
                        <li>Choco Curls/Cherries</li>
                    </ul>
                    <h6>Instructions</h6>
                    <ol>
                        <li>Grease an 8&ndash;inch round pan. Preheat oven to 350 degrees F. </li>
                        <li>In a bowl, slightly beat egg. Add in oil, water and MAYA Chocolate hotcake mix. Blend batter mixture until smooth. Pour into prepared pan. Place cake pan on oven tray and bake in the preheated oven for 30 minutes or until done. Cool down. Crumble cake to form coarse crumbs. Press unto bottom of container dish or clear plastic cups or any desired container. Set aside.</li>
                        <li>Prepare mousse, put cold water in a small bowl. Sprinkle gelatin. Let stand for 1 minute to soften. Add hot water. Stir until gelatin is completely dissolved and mixture is clear. Set aside.</li>
                        <li>In the steamer bowl, melt chocolate until thoroughly melted. Add gelatin mixture. Slightly cool. In a mixing bowl, whip cream until double in volume. Fold in the melted chocolate mixture. Pour unto prepared containers. Chill until ready to serve. Decorate; top with chocolate curls and cherries if desired.</li> 
                    </ol>
