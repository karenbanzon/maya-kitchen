                <h1>Blinis Shrimp Torte Canapes</h1>
                <img src="images/recipeImages/20131113-4.jpg">
                <h6>Ingredients</h6>
                Shrimp Torte:
                <ul>
                    <li>1 cup shrimps, deveined, shelled and cooked</li>
                    <li>1 package cream cheese</li>
                    <li>1/4 cup bacon bits</li>
                    <li>2 tablespoons green onions, chopped</li>
                    <li>2 tablespoons marsala wine</li>
                </ul>
                Blinis:
                <ul>
                    <li>1 piece egg</li>
                    <li>1 cup water</li>
                    <li>2 tablespoons oil</li>
                    <li>1 pack MAYA Whole Wheat Pancake Mix 200g</li>
                    <li>fresh dill</li>
                </ul>
                <h6>Procedure</h6>
                <p class="articleContent">Combine all ingredients for shrimp torte in a food processor and process until smooth and well blended. Set aside.

<strong>Prepare Blinis:</strong>
Combine egg, water and oil. Mix in pancake mix until well blended. Drop one tablespoon of pancake mixture onto a heated teflon coated pan. Do the same procedure with the rest of the mixture. Get one piece of blinis then put a little shrimp mixture on top then cover with another blinis and garnish on top as desired then serve.</p>
