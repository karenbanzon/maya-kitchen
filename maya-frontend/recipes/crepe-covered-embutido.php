                <h1>Crepe Covered Embutido</h1>
                <img src="images/recipeImages/20131205-1.jpg">
                <h6>Ingredients</h6>
                Crepe Batter:
                <ul>
                    <li>4 pieces eggs</li>
                    <li>1/4 cup melted butter</li>
                    <li>2 cups fresh milk</li>
                    <li>1 package MAYA Original &ldquo;Fluffy and Tasty&rdquo; Hotcake Mix 200 grams</li>
                </ul>
                Embutido:
                <ul>
                    <li>1/2 kilo ground pork</li>
                    <li>1/3 cup pickle relish</li>
                    <li>6 slices ham, finely chopped</li>
                    <li>1/4 cup raisins</li>
                    <li>1/4 cup tomato sauce</li>
                    <li>3 pieces eggs</li>
                    <li>1/2 cup grated cheese</li>
                    <li>2 tablespoons chopped green bell pepper</li>
                    <li>1/4 cup finely chopped carrots</li>
                    <li>salt and pepper to taste</li>
                </ul>
                <h6>Procedure</h6>
                <strong>Prepare Crepes:</strong>
                <ol>
                    <li>In a mixing bowl, beat eggs, melted butter and fresh milk until well combined then add in the hotcake mix.  </li>
                    <li>Keep stirring until no lumps and batter becomes smooth. </li>
                    <li>Use a 6-inch non-stick frying pan and ladle a scant of Â¼ cup crepe mixture. </li>
                    <li>Tilt the pan from sides to sides to have an even layer of crepe. Repeat the same procedure until all crepe batter is used. Set aside.</li>
                </ol>
                <strong>Prepare Embutido:</strong>
                <ol>
                    <li>In a bowl, combine all the ingredients and mix until well-blended.</li>
                    <li>Divide the mixture into 2 then wrap in aluminum foil or plastic. </li>
                    <li>Roll tightly and seal the ends.  </li>
                    <li>Place in a steamer and cook until done.  </li>
                    <li>Unwrap in foil then wrap with a layers of crepes. </li>
                    <li>Chill and serve.</li>
                </ol>
                <h6>Yield</h6>
                3 pieces medium-sized embutido
