                <h1>Cheesecake Crunch Bites</h1>
                <img src="images/recipeImages/20131113-3.jpg">
                <h6>Ingredients</h6>
                Base crumbs:
                <ul>
                    <li>1 cup Japanese crumbs</li>
                    <li>1/4 cup MAYA All&ndash;Purpose Flour</li>
                    <li>1/2 cup butter</li>
                    <li>1/3 cup grated parmesan</li>
                    <li>pepper to taste</li>
                    <li>1 tablespoon chopped parsley</li>
                </ul>
                Filling:
                <ul>
                    <li>1/2 cup dry white wine</li>
                    <li>1/4 cup minced shallots</li>
                    <li>1/2 cup cream cheese</li>
                    <li>1/2 cup cubed cheddar cheese</li>
                    <li>1 piece egg</li>
                </ul>
                Toppings:
                <ul>
                    <li>glazed sweet ham, diced</li>
                    <li>chopped spring onions</li>
                </ul>
                <h6>Procedure</h6>
                <p class="articleContent">Preheat oven to 350F/177C. Grease 1 ounce muffin tins. Set aside.
 
<strong>Prepare Base Crumbs:</strong>
Combine all ingredients in a food processor then pulse until well blended. Press into prepared muffin tins and bake for 5â€“10 minutes or until golden brown. Set aside.
 
<strong>Prepare Filling:</strong>
In a small saucepan, heat wine and shallots until liquid is reduced. Lower the heat and mix in the cheeses, keep stirring until well incorporated. Whisk in the egg until mixture become smooth. Set aside.
 
<strong>To Assemble:</strong>
Fill each prepared base crumbs with filling and top with glazed ham. Garnish with chopped spring onions.</p>
