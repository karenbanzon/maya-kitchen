                <h1>Herbed Fish En Papilotte</h1>
                <img src="images/recipeImages/20140115-2.jpg">
                <h6>Ingredients</h6>
                <ul>
                    <li>2 pieces fish fillet (sole fish, cat fish, cod or grouper)</li>
                    <li>salt and pepper</li>
                    <li>juice of 1 lemon</li>
                    <li>1 cup carrots, julienned</li>
                    <li>1/2 stalk leeks, thinly sliced</li>
                    <li>choice of 2 fresh herbs (dill, thyme, tarragon, basil, mint etc.)</li>
                    <li>few slices of lemon</li>
                    <li>2 tablespoons unsalted melted butter</li>
                    <li>2 tablespoons olive oil</li>
                </ul>
                <h6>Procedure</h6>
                <ol>
                    <li>Preheat oven to 375&deg;F / 190&deg;C.</li>
                    <li>Prepare and set aside two 12 x 14 inch parchment paper. Season fish fillets with salt, pepper, garlic and half of lemon juice.</li>
                    <li>Place and divide half of carrots at the center of each parchment papers. Place the seasoned fillets over the carrots.</li>
                    <li>On top of the fish, arrange the remaining carrots, leeks, herbs and sliced lemons. Drizzle garlic oil on top.</li>
                    <li>Thoroughly close and crimp all sides of each parchment paper to contain the steam.</li>
                    <li>Place on baking sheet and bake for 15- 20 minutes. Serve immediately.</li>
                </ol>
                <h6>Yield</h6>
                2 servings
                <h6>Note</h6>
                Steaming is another healthy cooking method that you can use for this recipe.
