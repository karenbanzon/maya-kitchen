            <div class="core recipe" id="singleArticle">
                <h1>Savory Chicken Pastel Puffs</h1>
                <img src="images/recipeImages/20131205-2.jpg">
                <h6>Ingredients</h6>
                Choux Pastry Puff:
                <ul>
                    <li>1 cup water</li>
                    <li>1/2 cup butter</li>
                    <li>1/2 teaspoon salt</li>
                    <li>1 cup MAYA All-Purpose Flour</li>
                    <li>4 pieces eggs</li>
                </ul>
                Pastel Filling:
                <ul>
                    <li>1 piece chicken breast, deboned and cut into serving pieces</li>
                    <li>3 tablespoons soy sauce</li>
                    <li>1 tablespoon lemon juice</li>
                    <li>salt and pepper to taste</li>
                    <li>1/2 cup butter</li>
                    <li>1 piece chorizo de bilbao, sliced</li>
                    <li>1 can Vienna sausage, sliced</li>
                    <li>1 cup broth</li>
                    <li>1/2 cup green olives</li>
                    <li>1 piece carrot, cubed</li>
                    <li>3 pieces potato, cubed</li>
                    <li>½ cup button mushrooms</li>
                    <li>2 tablespoons MAYA All-Purpose Flour</li>
                    <li>2 pieces eggs, hard-boiled, sliced</li>
                </ul>
                <h6>Procedure</h6>
                <strong>Prepare Puffs:</strong>
                <ol>
                    <li>Preheat oven to 400°F/ 204°C.  Grease and line cookie sheet.  Set aside.  </li>
                    <li>In a saucepan, combine water, butter and salt.  </li>
                    <li>Heat until butter melts and water boils.  </li>
                    <li>Turn heat to low, add flour all at once and stir vigorously until mixture leaves sides of pan.  Remove from heat then cool. </li>
                    <li>With the use of wooden spoon or electric mixer, beat in eggs one at a time beating until smooth after each addition.  </li>
                    <li>Continue until very smooth and shiny.  </li>
                    <li>Drop or pipe mixture using big star tip onto cookie sheet about 2 inches apart.  </li>
                    <li>Bake for 35 to 40 minutes or until choux puffs are brown, completely dry and with no bubbles.  Cool on wire racks. Slice the top portion and set aside. </li>
                </ol>
                <strong>Prepare Pastel Filling:</strong>
                <ol>
                    <li>Marinate chicken in soy sauce and lemon juice.  Season w/ salt and pepper.  </li>
                    <li>Melt butter in a skillet.  Brown chicken pieces.  </li>
                    <li>Add chorizo and sausage.  </li>
                    <li>Pour in the broth then cover and cook for a few minutes.  </li>
                    <li>Add in the olives, carrots, potatoes and mushrooms.  </li>
                    <li>Simmer until vegetables are cooked.  Thicken with flour.</li>
                </ol>
                <strong>Assemble:</strong>
                <ol>
                    <li>Place 1 big baked pastry puff on a serving plate then put pastel mixture to a baked pastry puff until almost flowing. </li>
                    <li>Serve while warm.</li>
                </ol>
                <h6>Yield</h6>
                5 big choux pastry puff / 5 servings
