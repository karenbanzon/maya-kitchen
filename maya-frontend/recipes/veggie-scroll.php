            <div class="core recipe" id="singleArticle">
                <h1>Veggie Scroll in Red Wine Vinaigrette</h1>
                <img src="images/recipeImages/20140115-3.jpg">
                <h6>Ingredients</h6>
                <ul>
                    <li>2-3 pieces medium-sized zucchini, cut lengthwise</li>
                    <li>olive oil, as needed</li>
                    <li>salt and pepper</li>
                    <li>1 piece grilled seasoned chicken breast, sliced (prepared)</li>
                    <li>1 piece julienne carrots, blanched</li>
                    <li>1 small bundle of asparagus spears, blanched (cut into 2-inch)</li>
                    <li>cherry tomatoes, halves</li>
                </ul>
                <strong>Vinaigrette Dressing</strong>
                <ul>
                    <li>3 tablespoons red wine vinegar</li>
                    <li>salt and pepper</li>
                    <li>1 tablespoon honey</li>
                    <li>1/2  teaspoon finely grated ginger</li>
                    <li>3 tablespoons olive oil</li>
                </ul>
                <h6>Procedure</h6>
                <ol>
                    <li>Preheat grill pan.</li>
                    <li>Brush the front and back of sliced zucchinis with olive oil and season it with salt and pepper. Grill until tender and grill marks are visible. Cool and set aside.</li>
                    <li>Lay one piece of grilled zucchini and place chicken, carrots and asparagus at about 1/2 inch from end of the zucchini slice then roll-up and place seam side down. Use toothpick to pierce and attach the tomatoes on top of the rolled zucchini.</li>
                    <li>Arrange in serving platter. Set aside.</li>
                </ol>
                <strong>Prepare Vinaigrette</strong>
                <p class="articleContent">In a bowl, combine the first 4 ingredients then slowly add in oil in slow steady stream, whisking continuously until it emulsifies. Drizzle on top of the veggie scrolls or serve it on the side.</p>
                <h6>Yield</h6>
                16-20 pieces
