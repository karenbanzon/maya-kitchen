            <div class="core recipe" id="singleArticle">
                <h1>Holiday Toasted Pandesal Pleasers</h1>
                <img src="images/recipeImages/20131205-3.jpg">
                <h6>Ingredients</h6>
                <ul>
                    <li>2 pieces red and yellow bell peppers</li>
                    <li>2-3 tablespoons olive oil</li>
                    <li>1 cup rocket leaves</li>
                    <li>3-4 pieces chopped basil leaves</li>
                    <li>2-3 tablespoons olive oil</li>
                    <li>2-3 tablespoons balsamic vinegar</li>
                    <li>salt and pepper to taste</li>
                    <li>pandesal, cut in halves, pan grilled</li>
                    <li>quezo de bola or parmesan shaves</li>
                    <li>alfalfa sprouts</li>
                </ul>
                <h6>Procedure</h6>
                <ol>
                    <li>Preheat oven to 450°F/230°C.  In a baking tray arrange bell peppers drizzle with olive oil and roast for 30 to 40 minutes or until roasted.  </li>
                    <li>When cool, remove skins and seeds and chop into strips.  Set aside.  </li>
                    <li>In a bowl, mix together peppers, cheese, rocket leaves and basil leaves. Set aside. </li>
                    <li>Put together olive oil, balsamic vinegar, salt and pepper in a salad jar or ordinary bottle and shake until fully blended. </li>
                    <li>Get grilled pandesal, top with peppers and green mixture, drizzle some olive oil mixture and arrange on top quezo de bola or parmesan shaves and alfalfa sprouts.  Serve.</li>
                </ol>
                <h6>Yield</h6>
                10 slices
