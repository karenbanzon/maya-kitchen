                <h1>Muffaletta Sandwich</h1>
                <img src="images/recipeImages/20131113-2.jpg">
                <h6>Ingredients</h6>
                BREAD:
                <ul>
                    <li>1 cup tap or warm water</li>
                    <li>1 tablespoon instant dry yeast</li>
                    <li>2 tablespoons sugar</li>
                    <li>3 cups MAYA All&ndash;Purpose Flour</li>
                    <li>1 1/2 teaspoons iodized salt</li>
                    <li>2 tablespoons vegetable shortening</li>
                    <li>sesame seeds as needed</li>
                    <li>1&ndash;2 tablespoons olive oil</li>
                </ul>
                EGG WASH:
                <ul>
                    <li>1 piece whole egg, beaten</li>
                    <li>2 tablespoons cold water</li>
                </ul>
                DRESSING:
                <ul>
                    <li>1/4 cup pitted Kalamata olives</li>
                    <li>1 tablespoon pickle relish</li>
                    <li>1 piece small onion</li>
                    <li>2 tablespoons capers</li>
                    <li>1 clove garlic</li>
                    <li>1/4 cup mayonnaise or olive oil</li>
                    <li>salt and pepper</li>
                </ul>
                FILLING
                <ul>
                    <li>cheese &mdash; swiss cheese, provolone cheese, mozzarella cheese</li>
                    <li>cured meat &mdash; mortadella, sliced ham, salami</li>
                    <li>greens &mdash; lettuce, tomatoes, green olives, cucumbers, alfalfa sprouts</li>
                </ul>
                <h6>Procedure</h6>
                <ol>
                    <li>In a mixer bowl, combine the water, yeast and sugar. Set aside for a few minutes until it foams. In another bowl, combine the flour, salt and vegetable shortening. Once the yeast is foamy, incorporate the flour mixture until well blended. Mix until dough leaves the sides of the bowl. Transfer dough to a lightly floured surface and knead until smooth and elastic. Put the dough into an olive oil greased bowl, and cover and let rise in a warm place until double in bulk, about 1 hour.</li>
                    <li>Punch down the dough and shape into a 9 inch flat round. Place the dough on a lightly oiled baking sheet then sprinkle top of dough with sesame seeds and lightly press the seeds to stick in the dough. Loosely cover the dough and let rise again in a warm place until double in bulk.</li>
                    <li>Remove cover and gently brush the dough with egg wash. Bake in a preheated oven 425&deg;F/218&deg;C for 10 minutes then lower the oven temperature to 375&deg;F/191&deg;C and bake again for another 20 minutes or until golden brown and sounds hallow when tapped. Cool completely on a wire rack before slicing.</li>
                    <li><strong>Prepare dressing:</strong> Simply combine all ingredients for dressing in a food processor then gradually add mayonnaise or olive oil while pulsing the processor a few times. Put the dressing in a bowl then season it with salt and pepper then set aside.</li>
                    <li><strong>Assemble:</strong> Slice the bread into two.Lay the bottom of the muffaletta bread on a work surface, cut side up and spread the dressing. Arrange half of the greens, meats cheeses on the bread. Top with the remaining meats and cheeses. Place top half of bread then press down and cut into four wedges.</li>
                </ol>
                <h6>Yield</h6>
                1 whole muffaletta sandwich (10&ndash;inch size), serves 10-12 pax
