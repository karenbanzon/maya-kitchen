<p>Regional cuisines from the Basque Country and the North of Spain take center stage on March 15, Saturday, 9am-1pm as Chef Jose Luis Gonzalez of renowned restaurant VASK visits The Maya Kitchen.

Chef Luis will demonstrate Pulpo a la Gallega Tradional or Galician Octopus is the signature dish of Galicia in the North of Spain. Octopus and potato are boiled and then garnished with paprika, rock salt and olive oil.

Pulpo a la Gallega Modern is a dish where the octopus is boiled, then smoked and grilled. The paprika is made with bread crumbs giving it a crispy texture. Instead of boiled potato, the potato is made into a foam.

Bakalao al Pil Pil with Pipperada is a traditional dish from the Basque Country (el Pais Vasco) and is well known all over Spain and popular with Spaniards and tourists alike. It is made with salt cod, garlic and olive oil. It is &ldquo;sabroso&rdquo; or tasty with the pil pil sauce, which is an emulsion sauce made with olive oill and natural gelatin of the fish. Piperrada is a typical Basque dishprepared with onion, green peppers, red bell peppers and tomatoes

Scallop Black Ink Risotto.  In the Basque Country they do not have paella, they have risotto instead. The dish is a modern risotto where the flavors are traditionally combined but cooked with a modern twist.

Chef Luis Gonzalez brings with him an impressive list of credentials. Hefinished his college culinary practice in the Michelin star restaurant Andra Mari.  He then joined Arzak in San Sebastian. Off to Mugaritz he went as Chef de Partie. He then became part of El Bulli, considered at the time as the world&rsquo;s number one restaurant. He was one of 35 chefs accepted out of the 80,000 who applied. He then went on a tour of restaurants in Southern France. Cellar de Can Rocca was his next stop then on to Nerua Restaurant at the Guggenheim Museum in Bilbao. He was also able to do an internship with Noma, another world famous restaurant.

Chef Luis found himself in the Philippines with hotels Sofitel and Shangri-la. Finally he is now with VASK, one of the rising stars of the local dining scene.

This special cooking demo is in cooperation with Mida Food Distributors, a leading supplier of seafoods to major hotels and restaurants with the onset of the Lenten Season, these recipes are welcome suggestions in menu planning and selection.
