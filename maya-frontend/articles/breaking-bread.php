<p>Ilustrado, the home of heritage Filipino&ndash;Spanish cuisine shares well loved dishes from our heroes&rsquo; hometowns at The Maya Kitchen, 9am&ndash;1pm on October 26, Saturday with Boni Pimentel, Ilustrado president and his chefs taking center stage.

Recipes include Umbuyan, tinapa flakes sauteed in olive oil and wrapped in pechay leaves has its origins in Manila where Andres Bonifacio was born.

Bagnet with KBL Sauce is an Ilocano version of lechon kawali with kamatis, bagoong and lasuna as sauce. Juan Luna and Antonio Luna are the proud sons of Ilocos Norte.

Manok ng mga Bayani, chicken simmered in bold textures of dill, kamias, ground peanuts and toasted rice is inspired by no less than President Manuel Luis Quezon who hailed from Baler, Aurora.

National hero Jose Rizal from Binan, Laguna inspired Jaleang Bayabas, very ripe guavas peeled and mashed in milk and sugar.

Rounding off the recipe line&ndash;up is Paella Ilustrado, Spanish rice dish with pork, chicken, seafood, chorizo bilbao, peppers and peas which is a consistent bestseller in the restaurant menu.

Ilustrado located at 744 General Luna Street, Intramuros, Manila opened its doors in 1989. Through the years it has expanded all over the metro and has also made a name in catering for clients such as Malacanang Palace and other government agencies, foreign embassies, multinational corporations, local businesses and prominent personalities. It is also a favorite wedding reception venue due in part to its proximity to some of Manila&rsquo;s historic churches.</p>
