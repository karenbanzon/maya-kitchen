<p>The Maya Kitchen presents the &lsquo;No Borders Cuisine&rsquo; of guest chef Vicky Pacheco, Chateau 1771 Group of Restaurants&rsquo; Executive Chef and Chief Operating Officer on August 31, Saturday, 9am&mdash;1pm.
 
Chateau 1771 Group of Restaurants is composed of Chateau, Caf&eacute;, Sentro, WineBar, SideBar and 1771 El Pueblo.
 
Chef Vicky will demonstrate a signature dish from each of the restaurant concept in the group. Lemon Chicken is crisp fried organic chicken leg nuggets coated with honey&mdash;lemon sauce from Chateau; Salbacho Pizza, a Sidebar staple, is topped with salami, bacon and chorizo; Fresh Smoked Fish Spring Rolls from Sentro is a fresh lumpia of tinapang bangus, salted egg, mustasa, onions and tomato; WineBar&rsquo;s Lacquered Salmon is grilled salmon steak with soy sauce and honey reduction, mashed potatoes with parmesan and Caf&eacute;&rsquo;s Bread & Butter Pudding is brioche type bread baked in a luscious custard with butter and real vanilla bean.
 
The first Chateau 1771 opened its doors in Malate in 1988 and has since relocated to its present location in Greenbelt 5. Its patented &ldquo;No Borders  Cuisine&rdquo; is a blend of well&mdash;loved Swiss, Italian and French creations.
 
Caf&eacute; 1771 in Ortigas is a beautiful dining destination serving truly unique sandwiches, breakfast, lasagna, stews, pizza and dessert.
 
WB 1771 is the group&rsquo;s Wine Bar concept with a wine selection from 11 countries and 48 regions while SideBar is the perfect place to unwind with great food and wine.
 
The group has not entirely left behind the Filipino palate with its trailblazing Sentro 1771 restaurant concept. It made famous sinigang na corned beef, rated GG and more.  The people behind it describe themselves as &lsquo;living, breathing and dreaming&rsquo; Filipino food. 
 
Chef Vicky first joined Chateau 1771 as Assistant Restaurant Manager in December 1988 and has since occupied several positions as Restaurant Manager, Executive Chef and Food and Beverage Manager.
 
She graduated with a Bachelor of Science in Hotel and Restaurant Administration from the University of the Philippines then got a Food and Beverage Diploma at the Les Roches, in Bluche&mdash;Montana, Switzerland.
 
Prior to joining Chateau 1771, she trained at The Manila Peninsula Hotel, the Hotel St. Georges and the Eurotel Riviera. She also worked as Assistant Restaurant Manager at the Puerto Galera Restaurant and Parola Bar in Daly City, South San Francisco, California, U.S.A.</p>
