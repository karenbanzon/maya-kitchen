<p><em>Nora Villanueva Daza</em> was born and raised in the province of Pampanga where her love for food began. Just like any girl growing up, she learned the basics of cooking at home from her grandmother, Dona Crescencia Reyes de Villanueva. To Nora, food was a passion and she was going to be a specialist at her craft. In 1952, she graduated with a Bachelor&rsquo;s degree in Home Economics from the University of the Philippines. Soon after, she pursued her Master of Science degree, major in Restaurant and Institutional Management at Cornell University in New York, where she earned membership in the Phi Kappa Phi Honor Society.
 
In 1965, trained in French cuisine by the partners of American French cooking legend Julia Child, Nora opened the first French restaurant here in Manila, Au Bon Vivant. She then opened a Filipino restaurant on 1970 in France, Aux Iles Philippines, which earned her a place in the Michelin guide, an annual guidebook for food lovers.
 
Nora had a very prosperous career as a chef, restaurateur, food columnist, cookbook author and TV and radio show host. In 1969, her first ever cookbook, &ldquo;Let&rsquo;s Cook with Nora&rdquo; was launched and became the housewives&rsquo; guide in cooking sumptuous meals for the families. She also had a television show in the 1980&rsquo;s, &ldquo;Cooking It Up with Nora&rdquo; and a radio program, &ldquo;At Home with Nora.&rdquo;
 
Amidst her busy schedule, Nora Daza was also part of the Liberty Commodities Corporation. She helped the company promote its Maya products through the Maya Bakeshop and by conducting cooking demonstrations all over the country. Maya was also one of her regular advertisers in her television shows. Later on, she became head of The Great Maya Cookfest and the culinary exchange tours of the company. She led the delegations of the cookfest winners in promoting the country through Filipino dishes.
 
Nora brought beauty, brains and grace to the culinary exchange programs. Her regal bearing was highlighted with her very bright personality, beautiful ternos she wore like royalty, and witty stories she shared that charmed everyone.
 
The Liberty Commodities Corporation would like to give its condolences to the Daza family, and to thank the great culinary icon for all the love and help she has given the company. We will forever be grateful. She has been an inspiration to many and will continue to be one in the decades to come. You will forever be remembered in our hearts, Ms. Nora V. Daza.</p>
